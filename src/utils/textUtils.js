export function generateText(mode, wordCount = 60) {
  if (mode === 'quotes') {
    const pool = WORD_POOLS.quotes
    return pool[Math.floor(Math.random() * pool.length)]
  }
  const pool = WORD_POOLS[mode] || WORD_POOLS.common
  const words = []
  for (let i = 0; i < wordCount; i++) {
    words.push(pool[Math.floor(Math.random() * pool.length)])
  }
  return words.join(' ')
}

export function calcWPM(correctChars, elapsedSeconds) {
  if (elapsedSeconds === 0) return 0
  return Math.round((correctChars / 5) / (elapsedSeconds / 60))
}

export function calcAccuracy(typed, text) {
  if (typed.length === 0) return 100
  let correct = 0
  for (let i = 0; i < typed.length; i++) {
    if (typed[i] === text[i]) correct++
  }
  return Math.round((correct / typed.length) * 100)
}

export function getRating(wpm) {
  if (wpm >= 80) return { label: '🚀 Excellent!',       cls: 'excellent' }
  if (wpm >= 60) return { label: '⚡ Great!',            cls: 'great'     }
  if (wpm >= 40) return { label: '👍 Good',              cls: 'good'      }
  return            { label: '📝 Keep Practicing',       cls: 'beginner'  }
}
